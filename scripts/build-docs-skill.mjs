#!/usr/bin/env node
// Generates the committed `using-wavex` agent skill from TSDoc + the curated
// guides (typedoc.json `projectDocuments`). Run via `pnpm docs:skill`, or
// `pnpm docs:check` (--check) to fail when the committed skill is stale.
import { spawnSync } from "node:child_process";
import { mkdirSync, mkdtempSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const skillDir = join(repoRoot, ".agents", "skills", "using-wavex");
const checkMode = process.argv.includes("--check");

const SKILL_MD = `---
name: using-wavex
description: WAVEx framework reference. Use when writing .wx templates or working
  with the @wavex/* package APIs (core parser/AST, compiler, runtime, vite-plugin,
  LSP, CLI). Includes the .wx language guides and the generated API reference.
---

<!-- Generated by scripts/build-docs-skill.mjs. Re-run \`pnpm docs:skill\` to update. -->

# Using WAVEx

WAVEx is a TypeScript-first framework: \`.wx\` files compile to Lit, with Convex
as the realtime backend, Web Awesome as the UI system, and Font Awesome for
icons.

## When to Use

- Writing or reviewing \`.wx\` templates (syntax, directives, Convex \`$\`/\`$$\`
  references, forms, components, utility shorthand).
- Working with any \`@wavex/*\` package API: parser/AST/diagnostics
  (\`@wavex/core\`), compilation (\`@wavex/compiler\`), the browser runtime
  (\`@wavex/runtime\`), the Vite+ plugin, the Volar LSP, or the \`wavex\` CLI.

## How to navigate

- \`_catalog.md\` indexes every page in this skill.
- Language guides (hand-curated, the authority on \`.wx\` syntax) are under
  \`api/documents/\`; the source files live in \`packages/core/docs/\`.
- The API reference under \`api/\` is generated from TSDoc; package landing
  pages carry the design intent for each package.

## Keeping it current

This skill is generated from the source tree. \`pnpm docs:skill\` regenerates it
in place; \`pnpm check\` includes a drift gate (\`pnpm docs:check\`) that fails
when source TSDoc or guides change without regeneration.
`;

function run() {
  const targetDir = checkMode ? mkdtempSync(join(tmpdir(), "wavex-docs-")) : skillDir;
  try {
    generate(targetDir);
    if (checkMode) {
      const differences = compareTrees(skillDir, targetDir);
      if (differences.length > 0) {
        console.error("docs:check failed — the committed using-wavex skill is stale:");
        for (const difference of differences) console.error(`  ${difference}`);
        console.error("Run `pnpm docs:skill` and commit the result.");
        process.exit(1);
      }
      console.log("docs:check passed — using-wavex skill is up to date.");
    } else {
      console.log(`using-wavex skill generated at ${relative(repoRoot, targetDir)}`);
    }
  } finally {
    if (checkMode) rmSync(targetDir, { recursive: true, force: true });
  }
}

function generate(targetDir) {
  mkdirSync(targetDir, { recursive: true });
  const typedoc = spawnSync(
    process.execPath,
    [join(repoRoot, "node_modules", "typedoc", "bin", "typedoc"), "--out", join(targetDir, "api")],
    { cwd: repoRoot, stdio: ["ignore", "pipe", "pipe"], encoding: "utf8" }
  );
  if (typedoc.status !== 0) {
    process.stderr.write(typedoc.stdout ?? "");
    process.stderr.write(typedoc.stderr ?? "");
    throw new Error(`typedoc exited with status ${typedoc.status}`);
  }
  writeFileSync(join(targetDir, "SKILL.md"), SKILL_MD);
  writeFileSync(join(targetDir, "_catalog.md"), buildCatalog(targetDir));
}

function buildCatalog(targetDir) {
  const lines = [
    "# using-wavex catalog",
    "",
    "<!-- Generated by scripts/build-docs-skill.mjs. -->",
    "",
    "| Page | Title |",
    "| --- | --- |"
  ];
  for (const file of listMarkdownFiles(targetDir)) {
    if (file === "SKILL.md" || file === "_catalog.md") continue;
    const title = firstHeading(join(targetDir, file)) ?? file;
    lines.push(`| ${file} | ${title} |`);
  }
  lines.push("");
  return lines.join("\n");
}

function firstHeading(filePath) {
  for (const line of readFileSync(filePath, "utf8").split("\n")) {
    const match = line.match(/^#+\s+(.*)/);
    if (match) return match[1].trim();
  }
  return undefined;
}

function listMarkdownFiles(root) {
  const files = [];
  const walk = (dir) => {
    for (const entry of readdirSync(dir, { withFileTypes: true }).sort((a, b) => a.name.localeCompare(b.name))) {
      const entryPath = join(dir, entry.name);
      if (entry.isDirectory()) walk(entryPath);
      else if (entry.name.endsWith(".md")) files.push(relative(root, entryPath));
    }
  };
  walk(root);
  return files.sort((a, b) => a.localeCompare(b));
}

function compareTrees(committedDir, generatedDir) {
  const differences = [];
  let committed;
  try {
    committed = new Map(listMarkdownFiles(committedDir).map((file) => [file, readFileSync(join(committedDir, file), "utf8")]));
  } catch {
    return ["skill directory is missing — run `pnpm docs:skill`"];
  }
  const generated = new Map(listMarkdownFiles(generatedDir).map((file) => [file, readFileSync(join(generatedDir, file), "utf8")]));
  for (const file of generated.keys()) {
    if (!committed.has(file)) differences.push(`missing: ${file}`);
    else if (committed.get(file) !== generated.get(file)) differences.push(`stale: ${file}`);
  }
  for (const file of committed.keys()) {
    if (!generated.has(file)) differences.push(`orphaned: ${file}`);
  }
  return differences;
}

run();
